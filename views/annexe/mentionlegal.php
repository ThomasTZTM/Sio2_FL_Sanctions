    Les partenaires attachent une attention toute particulière à votre information sur l’emploi des cookies. Aussi, cette page a pour but de vous sensibiliser à l’utilisation des cookies, vous informer de l’utilisation qui en est faite par le projet et vous indiquer comment paramétrer vos outils pour vous y opposer. <br>

    Qu’est-ce qu’un cookie ? <br>

    Un cookie est un petit fichier texte qui est déposé sur votre terminal informatique (ordinateur, tablette, smartphone…) et qui nous donne des informations relatives à votre connexion. Ces informations sont ensuite lues sur votre terminal à chacune de vos nouvelles visites. Elles peuvent être utilisées à différentes fins. Les cookies sont parfois nécessaires pour que nous puissions vous fournir nos services ou pour rendre votre navigation plus agréable. <br>

    Quels sont les cookies utilisés ? <br>

    * Les cookies de mesure d’audience et de statistiques. <br>

    Ce type de cookie est utilisé afin d’avoir un retour sur la fréquentation de ses sites et la façon dont ceux-ci sont utilisés. Les informations recueillies dans ce cadre nous permettent d’améliorer continuellement nos services, de les rendre compatibles avec les matériels utilisés, le tout afin de répondre au mieux à vos besoins. <br>

    * Les cookies utilisés pour vous délivrer nos services. <br>

    L’utilisation de ces cookies est techniquement nécessaire afin de vous fournir nos services. Il s’agit des cookies qui ont par exemple les fonctions suivantes : <br>

    Les cookies de session. Ils permettent de maintenir votre session active lorsque vous devez vous identifier. <br>
    Les cookies destinés à mémoriser vos préférences et permettant la personnalisation des services (par exemple celui nous permettant d’afficher votre nom sur votre interface). <br>
    Les cookies destinés à assurer la sécurité des services délivrés. <br>
    Comment puis-je m’opposer à l’utilisation d’un cookie ? <br>

    Vous pouvez accepter ou refuser les cookies utilisés. Pour cela vous pouvez paramétrer votre navigateur en conséquence. Nous vous informons toutefois qu’en désactivant l’utilisation des cookies, la qualité de votre navigation sur nos sites Internet peut être dégradée. Certains services sont également susceptibles de rencontrer des dysfonctionnements. Les partenaires et les prestataires ne sauraient en aucun en être tenu pour responsables. <br>

    Pour information, voici comment configurer les principaux navigateurs. Toutefois, ces outils étant susceptibles d’être modifiés ou d’évoluer, si vous rencontrez des difficultés dans leur paramétrage, merci de vous référer à la rubrique d'aide de votre navigateur. <br>


    Pour Microsoft Internet Explorer : <br>

    Choisissez le menu "Outils" (ou "Tools"), puis "Options Internet" (ou "Internet Options"). <br>

    Cliquez sur l'onglet " Confidentialité " (ou "Confidentiality"). <br>

    Sélectionnez le niveau souhaité à l'aide du curseur. <br>

    Pour plus de précisions vous pouvez consulter la page d'aide de l'éditeur du navigateur. <br>

    Pour Microsoft Edge : <br>

    Choisissez le menu « … » puis « Paramètres ». <br>

    Cliquez sur le menu « Confidentialité et sécurité ». <br>

    Sélectionnez le niveau souhaité à l'aide du menu déroulant « Cookies ». <br>

    Pour plus de précisions vous pouvez consulter la page d'aide de l'éditeur du navigateur. <br>

    Pour Mozilla Firefox : <br>

    Choisissez le menu "Outils" > "Options". <br>

    Cliquez sur l'option "Vie Privée" puis "Historique". <br>

    Pour plus de précisions vous pouvez consulter la page d'aide de l'éditeur du navigateur. <br>

    Pour Google Chrome : <br>

    Choisissez le menu "Paramètres" > "Afficher les paramètres avancés". <br>

    Dans la section "Confidentialité", cliquez sur "Paramètres de contenu" puis "Cookies". <br>

    Pour plus de précisions vous pouvez consulter la page d'aide de l'éditeur du navigateur. <br>

    Pour Safari (Mac OS) : <br>

    Choisissez le menu "Préférences" puis l'onglet "Sécurité". <br>

    Dans la zone "Accepter les cookies", cochez la case de votre choix. <br>

    Pour plus de précisions (iOS), vous pouvez consulter la page d'aide de l'éditeur du navigateur. <br>


